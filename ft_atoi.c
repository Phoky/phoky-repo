#include "libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int	r;
	int	f;

	i = 0;
	r = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if ((str[i] == '+' && str[i + 1] == '-') ||
			(str[i] == '-' && str[i + 1] == '+'))
		return (r);
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i++] == '-')
			f = 1;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		r = r * 10;
		r += (str[i++] - 48);
	}
	if (f == 1)
		r = -r;
	return (r);
}
