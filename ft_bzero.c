#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*b;

	i = 0;
	b = s;
	if (n == 0)
		return ;
	while (i < n)
	{
		b[i] = '\0';
		i++;
	}
}
