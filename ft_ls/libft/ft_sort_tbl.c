#include "libft.h"

void	ft_sort_tbl(char ***tbl)
{
	int	i;
	int	j;

	i = 0;
	j = 1;

	while (tbl[0][j])
	{
		if (ft_strcmp(tbl[0][j], tbl[0][i]) < 0)
		{
			ft_char_swap(&tbl[0][j], &tbl[0][i]);
			i = 0;
			j = 1;
		}
		else
		{
			i++;
			j++;
		}
	}
}
