#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	j;
	size_t	i;

	j = 0;
	i = 0;
	while (s1[i] != '\0')
		i++;
	while (j < n && s2[j] != '\0')
		s1[i++] = ((char *)s2)[j++];
	s1[i] = '\0';
	return (s1);
}
