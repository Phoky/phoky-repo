#include "libft.h"

int		ft_sqrt(int nb)
{
	int	i;
	int	c;
	int	r;

	c = nb;
	i = 1;
	if (nb < 0 || nb > 2147395600)
		return (0);
	while (c != 0)
	{
		r = i * i;
		if (r == nb)
			return (i);
		i++;
		c--;
	}
	return (0);
}
