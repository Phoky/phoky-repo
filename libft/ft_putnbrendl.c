#include "libft.h"

void	ft_putnbrendl(int n)
{
	int	i;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n == 0)
		write(1, "0", 1);
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n)
	{
		i = n % 10;
		n = n / 10;
		if (n)
			ft_putnbr(n);
		i = i + 48;
		write(1, &i, 1);
	}
	write(1, "\n", 1);
}
